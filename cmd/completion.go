package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

var completionCmd = &cobra.Command{
	Use:   "completion [bash|zsh|fish|powershell]",
	Short: "Generate shell completion scripts",
	Long: `Generate shell completion scripts for DevForge.

To load completions:

  Bash (add to ~/.bashrc):
    source <(devforge completion bash)

  Zsh (add to ~/.zshrc):
    source <(devforge completion zsh)

    # Or install permanently:
    devforge completion zsh > "${fpath[1]}/_devforge"

  Fish:
    devforge completion fish | source

    # Or install permanently:
    devforge completion fish > ~/.config/fish/completions/devforge.fish

  PowerShell (add to $PROFILE):
    devforge completion powershell | Out-String | Invoke-Expression`,
	DisableFlagsInUseLine: true,
	ValidArgs:             []string{"bash", "zsh", "fish", "powershell"},
	Args:                  cobra.MatchAll(cobra.ExactArgs(1), cobra.OnlyValidArgs),
	RunE: func(cmd *cobra.Command, args []string) error {
		switch args[0] {
		case "bash":
			return rootCmd.GenBashCompletion(os.Stdout)
		case "zsh":
			return rootCmd.GenZshCompletion(os.Stdout)
		case "fish":
			return rootCmd.GenFishCompletion(os.Stdout, true)
		case "powershell":
			return rootCmd.GenPowerShellCompletionWithDesc(os.Stdout)
		}
		return nil
	},
}

func init() {
	rootCmd.AddCommand(completionCmd)
}
